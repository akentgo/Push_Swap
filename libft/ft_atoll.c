#include "libft.h"

long long	ft_atoll(char *str)
{
	int			i;
	long long	nbr;
	int			s;

	i = 0;
	nbr = 0;
	s = 0;
	while (str[i] != '\0' && (str[i] == 32 || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f'))
		i++;
	if (str[i] != '\0' && str[i] == '-')
	{
		s = 1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] != '\0' && ft_isdigit(str[i]))
		nbr = (nbr * 10) + (str[i++] - '0');
	if (s == 1)
		return (-nbr);
	return (nbr);
}
